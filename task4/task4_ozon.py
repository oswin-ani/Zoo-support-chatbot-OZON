embedder = SentenceTransformer("sentence-transformers/paraphrase-multilingual-MiniLM-L12-v2")
emb = embedder.encode(gen_df["adversarial_prompt"].tolist(), normalize_embeddings=True)
sim = cosine_similarity(emb)

keep = np.ones(len(gen_df), dtype=bool)
for i in range(len(gen_df)):
    if not keep[i]:
        continue
    for j in range(i + 1, len(gen_df)):
        if sim[i, j] > 0.92:
            keep[j] = False

gen_df = gen_df.loc[keep].reset_index(drop=True)
gen_df["id"] = range(1, len(gen_df) + 1)

gen_df.to_csv(GEN_OUT_CSV, index=False, encoding="utf-8-sig")
